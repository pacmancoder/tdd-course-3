bool operator==(const Digit& lhs, const Digit& rhs)
{
    return lhs.lines_[0] == rhs.lines_[0]
            && lhs.lines_[1] == rhs.lines_[1]
            && lhs.lines_[2] == rhs.lines_[2];
}


bool operator!=(const Digit& lhs, const Digit& rhs)
{
    return !(lhs == rhs);
}

TEST(BankOCR, TwoDigitsAreEqual)
{
    Digit lhs({"|-|", "-|-", "   "});
    Digit rhs({"|-|", "-|-", "   "});

    EXPECT_EQ(lhs, rhs);
}

TEST(BankOCR, TwoDigitsAreNotEqual)
{
    Digit lhs({"|--", "-|-", "   "});
    Digit rhs({"|-|", "-|-", " | "});

    EXPECT_NE(lhs, rhs);
}


